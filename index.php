            <h2>Makanan Khas</h2>
            <p>
              Makanan khas DKI Jakarta pada dasarnya dipengaruhi oleh kuliner Betawi. Hal ini dikarenakan penduduk asli Jakarta notabenenya berasal dari suku Betawi. Suku Betawi merupakan sebuah suku bangsa di Indonesia yang umumnya bermukim di Batavia sejak abad ke-17.
              Bagi orang yang sudah lama menginjakkan kakinya di Jakarta, pasti sudah tak asing dengan berbagai makanan khas Betawi yang sangat beragam, unik, dan pastinya enak.
              Tapi untuk mereka yang belum pernah menghirup udara khas ibukota, mungkin akan terkesima mengetahui fakta akan keragaman masakan yang dimiliki oleh suku Betawi.
              Kondisi inilah yang menjadikan cita rasa makanan khas DKI Jakarta atau Betawi cukup beragam dan berkelas. Pastinya kenikmatan dan kelezatan dari makanan khas DKI Jakarta tak akan terbantahkan. Untuk itu, ketika berkunjung ke DKI Jakarta lama atau sebentar, tidak ada salahnya untuk mencicipi salah satu makanan khas DKI Jakarta yang terkenal.
              Berikut makanan khas DKI Jakarta yang terkenal:
            </p>
          </section>
          <h3>1.Kerak Telor</h3>
          <img
            src="assetsIndex/image/jajanan/keraktelor.jpe"
            class="featured-image"
            alt="Kerak Telor"
          />
          <p>
            Makanan khas DKI Jakarta pertama yang wajib dicobain adalah Kerak Telor. 
            Kerak telor tekenal dengan rasanya yang gurih. 
            Terbuat dari telur ayam atau bebek, beras ketan putih, 
            ebi yang disangrai hingga menyerupai abon, lalu ditambah bawang goreng dan diberi bumbu halus seperti kelapa sangrai.
          </p>

          <p>
            Kerak telor menjadi salah satu makanan yang sangat populer dan favorit bagi warga Jakarta. 
            Kerak telor hingga saat ini masih mudah dijumpai di banyak tempat di Jakarta. 
            Di Jakarta sendiri kamu bisa menemukannya di kawasan wisata Kota Tua. 
            Bahkan kerak telor juga telah banyak ditemui di berbagai daerah di luar Jakarta.
          </p>
        </section>
        <h3>2.Asinan Betawi</h3>
        <img
          src="assetsIndex/image/jajanan/asinanbetawi.jpg"
          class="featured-image"
          alt="Asinan Betawi"
        />
        <p>
          Bukan hanya Bogor yang memiliki asinan. 
          Ternyata Jakarta juga memiliki asinan dengan cita rasa yang tak kalah nikmat dengan asinan Bogor. 
          Pada Asinan Betawi memiliki campuran berbagai sayuran yang diasinkan dan diawetkan seperti kubis, 
          kecambah, tahu, sawi, selada ditambah dengan bumbu kacang yang sudah dicampur cuka dan cabai.
        </p>

        <p>
          Asinan ditaburi kacang goreng dan bisa disantap dengan kerupuk kuning berbentuk seperti mi yang melingkar, 
          biasanya kerupuk ini diremuk dan ditaburkan di atas asinan. 
          Meskipun namanya asinan, jangan sampai salah mengira karena makanan ini sama sekali tidak terasa asin 
          melainkan segar dan cocok disantap ketika siang hari.
        </p>
      </section>
      <h3>3.Soto Betawi</h3>
      <img
        src="assetsIndex/image/jajanan/sotobetawi.jpg"
        class="featured-image"
        alt="Soto Betawi"
      />
      <p>
        Soto merupakan salah satu jenis makanan yang dapat ditemui di berbagai daerah di Indonesia. 
        Setiap sajian soto di suatu daerah pasti memiliki cita rasanya sendiri. 
        Salah satu yang paling dikenal adalah Soto Betawi. 
        Soto Betawi merupakan salah satu makanan khas DKI Jakarta legendaris yang wajib di icip saat berkunjung ke Jakarta.
      </p>  

      <p>
        Istilah soto Betawi hadir dalam kuliner masakan Indonesia sekitar tahun 1977-1978, 
        yang memopulerkan dan yang pertama memakai kata soto Betawi adalah penjual soto bernama Lie Boen Po di THR Lokasari, 
        tentunya dengan ciri khas cita rasa sendiri.
        Kuah soto Betawi merupakan campuran santan dan susu yang membuat cita rasa soto ini begitu legendaris. 
        Soto betawi berisi jerohan dan seringkali organ-organ lain juga disertakan, seperti mata, terpedo, dan juga hati. 
        Daging sapi juga menjadi bahan campuran dalam soto Betawi.
      </p>
