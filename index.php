              alt="Pantai Ancol"
            />
            <p>
              Pantai Ancol merupakan kawasan wisata yang merupakan destinasi wisata pantai dalam kota Jakarta. Wisatawan dapat menikmati 
              suasana segar Pantai Lagoon, Festival, Indah, Beach Pool dan Carnaval, serta Danau Impian. Berlibur di alam terbuka dipercaya 
              memiliki efek menenangkan dan menyenangkan.Panjang garis pantainya yang kurang lebih 6,5 km menjadi tempat berbagai aktivitas 
              wisata. Wisata pantai ini merupakan BUMD penghasil Pendapatan Asli Daerah kedua terbesar bagi provinsi DKI Jakarta.
            </p>
