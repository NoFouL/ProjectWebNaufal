          <h2>Wisata</h2>
          <p>
            Jakarta merupakan salah satu destinasi wisata yang cukup baik di Indonesia. 
            Untuk meningkatkan jumlah wisatawan yang berkunjung ke Jakarta, 
            pemerintah mengadakan program "Enjoy Jakarta". 
            Beberapa tempat pariwisata yang terkenal dan biasa dikunjungi oleh para wisatawan lokal dan mancanegara
          </p>
