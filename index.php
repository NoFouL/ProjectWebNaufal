          <p>
            Nama "Jakarta" merupakan kependekan dari kata Jayakarta, 
            yaitu nama dari Bahasa Sanskerta yang diberikan oleh orang-orang Demak dan Cirebon 
            di bawah pimpinan Fatahillah (Faletehan) setelah menyerang dan berhasil menduduki pelabuhan Sunda Kelapa 
            pada tanggal 22 Juni 1527 dari Portugis.
          </p>

          <p>
            Nama ini diterjemahkan sebagai "kota kemenangan" atau "kota kejayaan". 
            Namun sejatinya berarti "kemenangan yang diraih oleh sebuah perbuatan atau usaha" 
            karena berasal dari dua kata Sanskerta yaitu Jaya yang berarti "kemenangan"dan Karta  yang berarti "dicapai".
          </p>
