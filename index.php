          <h2>Geografis</h2>
          <img
            src="assetsIndex/image/geografis.jpg"
            class="featured-image"
            alt="geografis"
          />
          <p>
            Jakarta berlokasi di sebelah utara Pulau Jawa, di muara Ciliwung, Teluk Jakarta. 
            Seluruh wilayah Jakarta terletak di dataran rendah pada ketinggian rata-rata 8 meter dpl 
            dengan titik tertinggi Jakarta adalah 91 meter dpl berada di Kawasan Buperta Cibubur, Cipayung, Jakarta Timur 
            yang merupakan ujung terendah dari formasi dataran Jonggol-Jatiluhur. 
            Sementara titik terendahnya yaitu -1 meter dpl dengan lokasi di wilayah Muara Baru dan Pluit, Jakarta Utara 
            dimana daerah tersebut mengalami fenomena penurunan tanah sejak lama.
          </p>

          <p>
            Jakarta memiliki suhu udara yang panas dan kering atau beriklim tropis. 
            Terletak di bagian barat Indonesia, Jakarta mengalami puncak musim penghujan pada bulan Januari dan Februari 
            dengan rata-rata curah hujan 350 milimeter dengan suhu rata-rata 27 째C. 
            Curah hujan antara bulan Januari dan awal Februari sangat tinggi, 
            pada saat itulah Jakarta dilanda banjir setiap tahunnya, 
            dan puncak musim kemarau pada bulan Agustus dengan rata-rata curah hujan 60 milimeter . 
            Bulan September dan awal Oktober adalah hari-hari yang sangat panas di Jakarta, 
            suhu udara dapat mencapai 40 째C . Suhu rata-rata tahunan berkisar antara 25째-38 째C.
          </p>
        </article>
